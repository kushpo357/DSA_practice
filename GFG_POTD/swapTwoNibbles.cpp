class Solution {
  public:
    int swapNibbles(int n) {
        // code here
        int nr = 15, nl = 240;
        nr = nr & n;
        nl = nl & n;
        nr = nr << 4;
        nl = nl >> 4;
        
        return nl + nr;
    }
};

