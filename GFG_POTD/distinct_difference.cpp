class Solution {
  public:
    vector<int> getDistinctDifference(int n, vector<int> &a) {
        // code here
         unordered_map<int, int> mp;
        
        for(int i = 0; i < n; i++) mp[a[i]]++;
        
        unordered_map<int, int> mp1;
        vector<int> ans;
        for(int i = 0; i < n; i++)
        {
            mp[a[i]]--;
            if(mp[a[i]] == 0) mp.erase(a[i]);
            ans.push_back(mp1.size() - mp.size());
            mp1[a[i]]++;
        }
        
        return ans;
    }
};


