class Solution{
  public:
    int cutRod(int price[], int n) {
        //code here
        
        vector<int> dp(n + 1, 0);
        
        for(int i = 1; i <= n; i++)
            for(int ind = 0; ind < i; ind++)
                dp[i] = max(dp[i], dp[ind] + price[i-ind-1]);
       
        return dp[n];
    }
};

