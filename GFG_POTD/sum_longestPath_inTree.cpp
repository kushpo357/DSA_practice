class Solution
{
public:
    
    int solve(Node* root, int &height)
    {
        if(root == NULL) return 0;
        
        if(root->left == NULL && root->right == NULL)
        {
            height++;
            return root->data;
        }
        
        int height1 = 0;
        int height2 = 0;
        
        int ans1 = solve(root->left, height1);
        int ans2 = solve(root->right, height2);
        
        
        if(height1 > height2)
        {
            height = height1 + 1;
            return ans1 + root->data;
        }
        if(height2 > height1)
        {
            height = height2 + 1;
            return ans2 + root->data;
        }
        
        height = height1 + 1;
        return root->data + max(ans1, ans2);
    }
    int sumOfLongRootToLeafPath(Node *root)
    {
        //code here
        int height = 0;
        return solve(root, height);
    }
};

