//Function to reverse the queue.
class Solution
{
    public:
    queue<int> rev(queue<int> q)
    {
        // add code here.
        int qn = q.size();
        vector<int> v(qn);
        
        for(int i = 0; i < qn; i++)
        {
            v[i] = q.front();
            q.pop();
        }
        
        reverse(v.begin(), v.end());
        
        for(int i = 0; i < qn; i++) q.push(v[i]);
        
        return q;
    }
};


